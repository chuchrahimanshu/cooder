// Import Section
import mongoose from "mongoose";

// Schema Section
const followSchema = new mongoose.Schema(
  {
    follower: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    following: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

// Mongoose Model
const Follow = mongoose.model("Follow", followSchema);

// Export Section
export { Follow };
